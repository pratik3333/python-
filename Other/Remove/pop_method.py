                                # Remove Specified Index
#If you do not specify the index, the pop() method removes the last item.

#Remove the last item
test_list=["hey","man","how","are","you"]
test_list.pop()
print(test_list)

# OP:-
# ['hey', 'man', 'how', 'are']



# The pop() method removes the specified index.

test_list=["hey","man","how","are","you"]
test_list.pop(2)
print(test_list)

#
# OP:-
# ['hey', 'man', 'are', 'you']

